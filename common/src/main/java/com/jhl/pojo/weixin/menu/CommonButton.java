package com.jhl.pojo.weixin.menu;

/**
 * 普通按钮（子按钮）
 * @author liurui
 * @2016年3月5日 @下午5:44:59
 */
public class CommonButton extends Button{
	//按钮类型
	private String type;

	//按钮key
	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}



}
